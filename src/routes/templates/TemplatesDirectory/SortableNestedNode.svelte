<script lang="ts">
    import CartPlusIcon from "$lib/icons/CartPlusIcon.svelte"
    import FolderClosedIcon from "$lib/icons/FolderClosedIcon.svelte"
    import FolderOpenIcon from "$lib/icons/FolderOpenIcon.svelte"
    import showdown from "showdown"
    import Sortable, { type SortableEvent } from "sortablejs"
    import { createEventDispatcher, onMount } from "svelte"
    import { slide } from "svelte/transition"
    import type { DecisionTree } from "../DecisionTree"
    import AdminButtons from "./AdminButtons.svelte"

    export let node: DecisionTree
    export let editable = false
    export let isRoot = true

    let isFolded = !isRoot
    let sorter: Sortable

    const dispatch = createEventDispatcher()

    function fold() {
        if (isRoot) return
        isFolded = !isFolded
    }

    function onSave() {
        dispatch("save", { id: node.id })
    }

    onMount(() => {
        const rootElem = document.getElementById(`node-${node.id}`)
        if (!rootElem) return
        if (node.children.length === 0) return
        sorter = Sortable.create(rootElem, {
            group: {
                name: `node-${node.id}`,
                put: true,
                pull: true
            },
            disabled: true,
            animation: 100,
            onEnd: (event: SortableEvent) => {
                dispatch("drag", {
                    oldIndex: event.oldIndex,
                    newIndex: event.newIndex,
                    to: event.to,
                    from: event.from,
                    item: event.item
                })
            }
        })
    })

    function markdownToSkeletonHtml(markdown: string): string {
        const converter = new showdown.Converter({ tables: true })
        const tags = ["h1", "h2", "h3", "h4", "h5", "h6"]

        let html = converter.makeHtml(markdown)
        tags.forEach(tag => {
            const tagRegex = new RegExp(`<${tag}`, "g")
            html = html.replace(tagRegex, `<${tag} class="${tag}"`)
        })

        html = html.replace(/<table/g, '<div class="table-container"><table class="table table-compact table-hover"')
        html = html.replace(/<\/table>/g, "</table></div>")
        return html
    }

    $: sorter?.option("disabled", !editable)
</script>

<div id={`node-${node.id}`}>
    <div>
        <!-- Inner div is necessary because otherwise the child elements are individually draggable.-->
        <div class="flex items-center space-x-1" transition:slide>
            <button class="hover-highlight" on:click={node.children.length === 0 ? onSave : fold}>
                {#if node.children.length === 0}
                    <CartPlusIcon class="text-secondary-400" />
                {:else if isFolded}
                    <FolderClosedIcon class="text-secondary-900" />
                {:else}
                    <FolderOpenIcon class="text-secondary-600" />
                {/if}
            </button>
            <div class="overflow-y-scroll max-h-[200px]">
                <span tabindex="0" role="textbox" aria-multiline="true">
                    {@html markdownToSkeletonHtml(node.text)}
                </span>
            </div>
            {#if editable}
                <div>
                    <AdminButtons bind:node showDelete={!isRoot} showEdit={!isRoot} />
                </div>
            {/if}
        </div>
        {#if !isFolded}
            <div class="border-secondary-500 border-l-2 pl-3 mb-2" transition:slide>
                {#each node.children as child}
                    {#key child.id}
                        <svelte:self bind:node={child} bind:editable isRoot={false} on:drag on:save on:create />
                    {/key}
                {/each}
            </div>
        {/if}
    </div>
</div>
